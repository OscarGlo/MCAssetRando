from typing import Sequence


def compare_versions(ver1: Sequence[int], ver2: Sequence[int]):
    for i in range(min(len(ver1), len(ver2))):
        if ver1[i] != ver2[i]:
            return ver1[i] - ver2[i]
    return len(ver1) - len(ver2)


